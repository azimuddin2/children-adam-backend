import { model, Schema } from 'mongoose';
import { TNews } from './news.interface';

const NewsSchema = new Schema<TNews>(
  {
    postTitle: {
      type: String,
      required: true,
      trim: true,
    },
    subTitle: {
      type: String,
      required: true,
      trim: true,
    },
    description: {
      type: String,
      required: true,
      trim: true,
    },
    image: {
      type: String,
      required: true,
    },
    isDeleted: {
      type: Boolean,
      default: false,
    },
  },
  { timestamps: true },
);

export const News = model<TNews>('News', NewsSchema);
